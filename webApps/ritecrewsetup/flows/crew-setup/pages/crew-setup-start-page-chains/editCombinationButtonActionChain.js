define([
  'vb/action/actionChain',
  'vb/action/actions',
  'vb/action/actionUtils',
], (
  ActionChain,
  Actions,
  ActionUtils
) => {
  'use strict';

  class editCombinationButtonActionChain extends ActionChain {

    /**
     * @param {Object} context
     * @param {Object} params
     * @param {object} params.event
     * @param {any} params.originalEvent
     */
    async run(context, { event, originalEvent }) {
      const { $page, $flow, $application, $constants, $variables } = context;

      $variables.combinationObj = $variables.combinationSelectedRow;
      $variables.edit = false;

      const combinationFormOpen = await Actions.callComponentMethod(context, {
        selector: '#combinationForm',
        method: 'open',
      });
    }
  }

  return editCombinationButtonActionChain;
});
